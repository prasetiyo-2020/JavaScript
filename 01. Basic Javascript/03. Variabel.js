// Deklarasi variabel dengan var
var lastName;
lastName = "Prasetiyo";
console.log(lastName);
// Output : Prasetiyo

// Deklarasi variabel dengan let
let lastName = "Prasetiyo";
console.log(lastName);
// Output : Prasetiyo

// Deklarasi variabel dengan const
const lastName = "Prasetiyo";
console.log(lastName);
// Output : Prasetiyo
